public class Day5{
    public static void main(String[] args){
        // ===== TIPE DATA BYTE =====
        byte umur = 18; 
        System.out.println("umur (byte); "+umur);
        // ===== TIPE DATA INT =====
        int jumlahpenduduk = 1800000000;
        System.out.println("jumlah penduduk indonesia (int): "+jumlahpenduduk);
        // ===== TIPE DATA LONG =====
        long jarakbangunan = 12345678910L;
        System.out.println("jarak bangunan terdkat ke bumi (long): " + jarakbangunan+ " km");
        // ===== CONTOH PERHITUNGAN =====
        int a = 100;
        int b = 200;
        int hasil = a + b;
        System.out.println("hasil penjumlahan (int): " + hasil);
        // ====== TIPE DATA SHORT ======
        short jumlahpendudukdikotamajene = 1234;
        System.out.println("jumlah penduduk dikota majene (short): " + jumlahpendudukdikotamajene); 
    }
}