public class Day9 {
    public static void main(String[] args) {
        // Deklarasi dan inisialisasi variabel
        int umur = 15;
        double tinggi_badan = 155;
        // Menampilkan nilai awal
        System.out.println("nilai awal umur: " + umur);
        System.out.println("tinggi badan awal: " + tinggi_badan);
        System.out.println(); // baris kosong

        // Update nilai variabel
        umur = 18;
        tinggi_badan = 165;
        // Menampilkan nilai setelah update
        System.out.println("setelah update");
        System.out.println("umur: " + umur);
        System.out.println("tinggi badan: " + tinggi_badan);
    }
}